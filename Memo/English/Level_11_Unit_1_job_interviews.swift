 嗯，我需要再面试一个候选人。到下个星期一我应该会告诉你我的决定。
 
 
 Thank you for taking the time to interview me. I hope to talk to you again soon.
 
 感谢您抽时间来面试我。我希望我们会很快再通话。
 
 
 Oh, and could I have one of your business cards?
 
 哦，我能要一张您的名片吗？
 
 */


